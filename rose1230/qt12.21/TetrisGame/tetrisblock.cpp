void TetrisBlock::rotateBlock(int block[4][4]) {
    int temp[4][4] = {0};
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            temp[j][3 - i] = block[i][j];
        }
    }
    memcpy(block, temp, sizeof(temp));
}
